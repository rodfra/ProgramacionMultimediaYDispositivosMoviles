/**
 * 0101 - Operaciones básicas
 * Muestra la suma, resta, multiplicación y división de dos enteros largos
 * prefijados, así como el resto de dividir el primero entre el segundo.
 */

fun main(args: Array<String>) {
    val num1: Double = 11.11
    val num2: Double = 22.22

    println("Suma de $num1 + $num2 = " + (num1 + num2))
    println("Resta de $num1 - $num2 = " + (num1 - num2))
    println("Multiplciación de $num1 x $num2 = " + (num1 * num2))
    println("División de $num1 / $num2 = " + (num1 / num2))
    println("Resto de $num1 % $num2 = " + (num1 % num2))
}
